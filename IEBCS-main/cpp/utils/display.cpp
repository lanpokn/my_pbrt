DisplaySRGB::DisplaySRGB(const std::vector<float>& ww, const std::vector<cv::Point3f>& ss, const std::vector<cv::Point3f>& gg)
{
    wave = ww;
    spd = ss;
    gamma = gg;
    // calculate colorspace
    size_t num = wave.size();
    std::vector<cv::Point3f> S = Color::readSpectraXYZ(wave);
    std::vector<float> dWave = calWaveBandLen<float>(wave);
    cv::Point3f r(0, 0, 0);
    cv::Point3f g(0, 0, 0);
    cv::Point3f b(0, 0, 0);
    for (size_t i = 0; i < num; i++)
    {
        r.x += spd[i].x * S[i].x * dWave[i];
        r.y += spd[i].x * S[i].y * dWave[i];
        r.z += spd[i].x * S[i].z * dWave[i];

        g.x += spd[i].y * S[i].x * dWave[i];
        g.y += spd[i].y * S[i].y * dWave[i];
        g.z += spd[i].y * S[i].z * dWave[i];

        b.x += spd[i].z * S[i].x * dWave[i];
        b.y += spd[i].z * S[i].y * dWave[i];
        b.z += spd[i].z * S[i].z * dWave[i];
    }
    cv::Point3f w = r + g + b;

    cs.r[0] = r.x / (r.x + r.y + r.z);
    cs.r[1] = r.y / (r.x + r.y + r.z);
    cs.g[0] = g.x / (g.x + g.y + g.z);
    cs.g[1] = g.y / (g.x + g.y + g.z);
    cs.b[0] = b.x / (b.x + b.y + b.z);
    cs.b[1] = b.y / (b.x + b.y + b.z);
    cs.w[0] = w.x / (w.x + w.y + w.z);
    cs.w[1] = w.y / (w.x + w.y + w.z);
    maxLuminance = 683.0 * (r.y + g.y + b.y);
}
SceneSpectral DisplaySRGB::rendering(const SceneRGB& rgb, std::vector<float> waveDst) const
{
    SceneSpectral spec;
    if (wave.empty() || spd.empty() || gamma.empty() || rgb.rgb.empty())
    {
        return spec;
    }
    if (wave.size() != spd.size())
    {
        fprintf(stderr, "error: the wave and spd of the display have the different size, faield to display. \n");
        return spec;
    }
    if (waveDst.empty())
    {
        waveDst.resize(31);
        for (int i = 0; i < 31; i++)
        {
            waveDst[i] = 400.0f + 10.0f * i;
        }
    }
    // calculate spd in accordance with waveDst
    std::vector<cv::Point3f> spdDst;
    if (waveDst == wave)
    {
        spdDst = spd;
    }
    else
    {
        int num = waveDst.size();
        int spdNum = (int)spd.size();
        std::vector<float> spdCh(spdNum);
        std::vector<float> spdDstTmp;
        spdDst.resize(num);
        // x channel
        for (int s = 0; s < spdNum; s++)
        {
            spdCh[s] = spd[s].x;
        }
        spdDstTmp = interp(wave, spdCh, waveDst);
        for (int d = 0; d < num; d++)
        {
            spdDst[d].x = spdDstTmp[d];
        }
        // y channel
        for (int s = 0; s < spdNum; s++)
        {
            spdCh[s] = spd[s].y;
        }
        spdDstTmp = interp(wave, spdCh, waveDst);
        for (int d = 0; d < num; d++)
        {
            spdDst[d].y = spdDstTmp[d];
        }
        // z channel
        for (int s = 0; s < spdNum; s++)
        {
            spdCh[s] = spd[s].z;
        }
        spdDstTmp = interp(wave, spdCh, waveDst);
        for (int d = 0; d < num; d++)
        {
            spdDst[d].z = spdDstTmp[d];
        }
    }
    // convert non-srgb to srgb
    std::vector<cv::Mat_<float>> srgb = Color::rgb2srgbVec<float>(rgb.rgb, rgb.getGamma(), rgb.getColorSpace());
    srgb[0] *= 1.0 / 255;
    srgb[1] *= 1.0 / 255;
    srgb[2] *= 1.0 / 255;
    int rows = srgb[0].rows;
    int cols = srgb[0].cols;
    // cal spectral radiance according to the display
    const int bandNum = waveDst.size();
    const int gammaNum = (int)gamma.size();
    std::vector<cv::Mat_<float>> energy(bandNum);
    int gIdx = 0;
#if NDEBUG
#pragma omp parallel for
#endif
    for (int b = 0; b < bandNum; b++)
    {
        cv::Mat_<float> curBand = cv::Mat_<float>::zeros(rows, cols);
        for (int r = 0; r < rows; r++)
        {
            for (int c = 0; c < cols; c++)
            {
                float tmp = 0.0f;
                gIdx = round(std::min(srgb[0].at<float>(r, c), 1.0f) * (gammaNum - 1));
                tmp += spdDst[b].x * gamma[gIdx].x;
                gIdx = round(std::min(srgb[1].at<float>(r, c), 1.0f) * (gammaNum - 1));
                tmp += spdDst[b].y * gamma[gIdx].y;
                gIdx = round(std::min(srgb[2].at<float>(r, c), 1.0f) * (gammaNum - 1));
                tmp += spdDst[b].z * gamma[gIdx].z;
                curBand.at<float>(r, c) = tmp;
            }
        }
        energy[b] = curBand;
    }
    // energy to photons
    spec.photons = Color::energy2Quanta<float, float, float>(energy, waveDst);
    spec.waves = waveDst;
    spec.mfov = rgb.mfov;
    spec.depth = rgb.depth.clone();
    spec.distance = rgb.distance;
    spec.setMeanLuminance(Color::calMeanLuminance<float>(spec.photons, spec.waves));
    return spec;
}

